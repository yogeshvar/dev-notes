#include <iostream>

using namespace std;

int main()
{
    int a, b;
    cout << "Enter two numbers" << endl;
    cin >> a >> b;

    cout << "Sum of two numbers is " << a + b << endl;

    cout << "Subtraction of two numbers is " << a - b << endl;

    cout << "Multiplication of two numbers is " << a * b << endl;

    return 0;
}